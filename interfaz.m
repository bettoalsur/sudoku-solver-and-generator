% --- Executes on button press in comprobar.
function comprobar_Callback(hObject, eventdata, handles)
% hObject    handle to comprobar (see GCBO)

global solucion mat revisar ayuda

if strcmp('OK',get(handles.comprobar,'string'))
    criterio=1; %Boton OK
else
    criterio=2; %Boton comprobar
end

switch criterio

    case 1
        set(handles.comprobar, 'string','Comprobar')
        num=1;
        for i=1:9
            for j=1:9
                if mat(i,j)==0 && revisar(i,j)==solucion(i,j)
                    set(handles.(sprintf('edit%d',num)), 'Foregroundcolor',[0.5,0.5,0.5]);
                    set(handles.(sprintf('edit%d',num)), 'enable','inactive')
                    set(handles.(sprintf('edit%d',num)),'string',num2str(revisar(i,j)))
                elseif revisar(i,j)~=0 && revisar(i,j)~=solucion(i,j) && mat(i,j)==0
                    set(handles.(sprintf('edit%d',num)), 'Foregroundcolor',[0.5,0.5,0.5]);
                    set(handles.(sprintf('edit%d',num)), 'enable','on')
                    set(handles.(sprintf('edit%d',num)),'string','')
                elseif mat(i,j)==solucion(i,j)
                    set(handles.(sprintf('edit%d',num)), 'Foregroundcolor',[0,0,0]);
                    set(handles.(sprintf('edit%d',num)), 'enable','inactive')
                    set(handles.(sprintf('edit%d',num)),'string',num2str(mat(i,j)))
                else
                    set(handles.(sprintf('edit%d',num)), 'Foregroundcolor',[0.5,0.5,0.5]);
                    set(handles.(sprintf('edit%d',num)), 'enable','on')
                    set(handles.(sprintf('edit%d',num)),'string','')
                end
            num=num+1;
            end
        end
        
        set(handles.ingresar,'enable','off');
        set(handles.generar,'enable','off');
        set(handles.limpiar, 'enable','on');
        set(handles.resolver, 'enable','on');
        set(handles.vaciar, 'enable','on');
        set(handles.boton1, 'enable','off');
        set(handles.boton2, 'enable','off');
        set(handles.boton3, 'enable','off');
        set(handles.boton4, 'enable','off');
        set(handles.comprobar, 'string','Comprobar')
        
        ayuda=ayuda-1;
        
        if ayuda==1
            set(handles.ayuda1, 'Backgroundcolor','red');
            set(handles.ayuda2, 'Backgroundcolor','green');
            set(handles.comprobar, 'enable','on');
        elseif ayuda==0
            set(handles.ayuda1, 'Backgroundcolor','red');
            set(handles.ayuda2, 'Backgroundcolor','red');
            set(handles.comprobar, 'enable','off');
        end
        
    case 2
        
        revisar=zeros(9,9);
        num=1;
        for i=1:9
            for j=1:9
                revisar(i,j)=str2double(get(handles.(sprintf('edit%d',num)),'string'));
                num=num+1;
            end
        end

        AA=isnan(revisar);
        revisar(AA)=0;

        num=1;
        for i=1:9
            for j=1:9
                if mat(i,j)==0 && revisar(i,j)==solucion(i,j)
                    set(handles.(sprintf('edit%d',num)), 'Foregroundcolor',[0,0.5,0]);
                    set(handles.(sprintf('edit%d',num)), 'enable','inactive')
                    set(handles.(sprintf('edit%d',num)),'string',num2str(revisar(i,j)))
                elseif revisar(i,j)~=0 && revisar(i,j)~=solucion(i,j) && mat(i,j)==0
                    set(handles.(sprintf('edit%d',num)), 'Foregroundcolor','red');
                    set(handles.(sprintf('edit%d',num)), 'enable','inactive')
                    set(handles.(sprintf('edit%d',num)),'string',num2str(revisar(i,j)))
                elseif mat(i,j)==solucion(i,j)
                    set(handles.(sprintf('edit%d',num)), 'Foregroundcolor',[0,0,0]);
                    set(handles.(sprintf('edit%d',num)), 'enable','inactive')
                    set(handles.(sprintf('edit%d',num)),'string',num2str(mat(i,j)))
                else
                    set(handles.(sprintf('edit%d',num)), 'Foregroundcolor',[0.5,0.5,0.5]);
                    set(handles.(sprintf('edit%d',num)), 'enable','inactive')
                    set(handles.(sprintf('edit%d',num)),'string','')
                end
            num=num+1;
            end
        end

        set(handles.ingresar,'enable','off');
        set(handles.generar,'enable','off');
        set(handles.limpiar, 'enable','off');
        set(handles.resolver, 'enable','off');
        set(handles.comprobar, 'enable','on');
        set(handles.vaciar, 'enable','off');
        set(handles.boton1, 'enable','off');
        set(handles.boton2, 'enable','off');
        set(handles.boton3, 'enable','off');
        set(handles.boton4, 'enable','off');
        set(handles.comprobar, 'string','OK')
    
end



