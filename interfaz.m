% --- Executes on button press in resolver.
function resolver_Callback(hObject, eventdata, handles)
% hObject    handle to resolver (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
global mat solucion

if strcmp('off',get(handles.comprobar, 'enable'))
    solucion=resol1(mat);
    if numel(solucion(solucion==0))~=0
        solucion=adivinar(solucion);
    end
end

num=1;
for i=1:9
    for j=1:9
        if mat(i,j)==0
            set(handles.(sprintf('edit%d',num)), 'Foregroundcolor',[0.5,0.5,0.5]);
            set(handles.(sprintf('edit%d',num)), 'string',num2str(solucion(i,j)));
            set(handles.(sprintf('edit%d',num)), 'enable','inactive');
        else
            set(handles.(sprintf('edit%d',num)), 'Foregroundcolor',[0,0,0]);
            set(handles.(sprintf('edit%d',num)), 'string',num2str(mat(i,j)));
            set(handles.(sprintf('edit%d',num)), 'enable','inactive');
        end
        num=num+1;
    end
end

set(handles.ingresar,'enable','off');
set(handles.generar,'enable','off');
set(handles.limpiar, 'enable','off');
set(handles.resolver, 'enable','off');
set(handles.comprobar, 'enable','off');
set(handles.vaciar, 'enable','on');
set(handles.boton1, 'enable','off');
set(handles.boton2, 'enable','off');
set(handles.boton3, 'enable','off');
set(handles.boton4, 'enable','off');
set(handles.ayuda1, 'Backgroundcolor',[0.94,0.94,0.94]);
set(handles.ayuda2, 'Backgroundcolor',[0.94,0.94,0.94]);

